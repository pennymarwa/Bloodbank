<h3 style="color:#F00; font-size:20px;"><u><b>Beat The Myth</b></u></p></h3><br /><br />
<p>Donating blood is safe and simple. It takes approximately 10-15 minutes to complete the blood donation process. Any healthy adult between 18 years and 60 years of age can donate blood. This is what you can expect when you are ready to donate blood:</p>
<ul>
<li>You walk into a reputed and safe blood donation centre or a mobile camp organized by a reputed institution.<br /></li>
<li>A few questions will be asked to determine your health status (general questions on health, donation history etc). Usually you will be asked to fill out a short form.<br /></li>
<li>Then a quick physical check will be done to check temperature, blood pressure, pulse and hemoglobin content in blood to ensure you are a healthy donor.<br /></li>
<li>If found fit to donate, then you will be asked to lie down on a resting chair or a bed. Your arm will be thoroughly cleaned. Then using sterile equipments blood will be collected in a special plastic bag. Approximately 350 ml of blood will be collected in one donation. Those who weigh more than 60 Kg can donate 450 ml of blood.<br /></li>
<li>Then you must rest and relax for a few minutes with a light snack and something refreshing to drink. Some snacks and juice will be provided.<br /></li>
<li>Blood will be separated into components within eight hours of donation.</li>
<li>The blood will then be taken to the laboratory for testing.<br /></li>
<li>Once found safe, it will be kept in special storage and released when required.</li>
<li>The blood is now ready to be taken to the hospital, to save lives.</li>
</ul>


<h3 style="color:#F00; font-size:20px;"><u><b>Blood Groups</b></u></h3><br /><br />
<p>Blood type is determined by which antibodies and antigens the person's blood produces. An individual has two types of blood groups namely ABO-grouping and Rh-grouping. Rh is called as the Rhesus factor that has come to us from Rhesus monkeys.</p>

<br />
Most humans are in the ABO blood group. The ABO group has four categories namely<br /> 
1) A group 2) B group 3) O group and 4) AB group
<br />
In the Rh- group, either the individual is said to be Rh- Negative or Rh- Positive.<br /><br />
<br />

<p>Thus blood group of any human being will mainly fall in any one of the following groups.</p>
<p>
A positive or A negative <br />
B positive or B negative <br />
O positive or O negative <br />
AB positive or AB negative.<br /><br /></p>
<h3 style="color:#F00; font-size:20px;"><u><b>Universal Donors and Recipients</b></u></h3><br /><br />

<p>The most common blood type is O, followed by type A.</p><br /><br />
<p>Type O individuals are often called "universal donors" since their blood can be transfused into persons with any blood type. Those with type AB blood are called "universal recipients" because they can receive blood of any type.</p><br /><br />
<p>However, since approximately twice as many people in the general population have O and A blood types, a blood bank's need for this type of blood increases exponentially.</p><br />


<h3 style="color:#F00; font-size:18px;"><u><b>DO donate blood, only if you satisfy all of the following conditions</b></u></h3><br /><br />

<font color="#00CC00" size="+3">&radic;</font>You are between age group of 18-60 years.<br />
<font color="#00CC00" size="+3">&radic;</font>Your weight is 45 kgs or more.<br />
<font color="#00CC00" size="+3">&radic;</font>Your hemoglobin is 12.5 gm% minimum.<br />
<font color="#00CC00" size="+3">&radic;</font>Your last blood donation was 3 or more months earlier.<br />
<font color="#00CC00" size="+3">&radic;</font>You are healthy and have not suffered from malaria, typhoid or other transmissible disease in the recent past..<br />


<h3 style="color:#F00; font-size:20px;"><u><b>DO NOT donate blood, if you have any of the following conditions</b></u></h3><br /><br />

<font color="#FF3300" size="+3">&times;</font> Cold / fever in the past 1 week.<br />
<font color="#FF3300" size="+3">&times;</font> Under treatment with antibiotics or any other medication.<br />
<font color="#FF3300" size="+3">&times;</font> Cardiac problems, hypertension, epilepsy, diabetes (on insulin therapy), history of cancer, chronic kidney or liver disease, bleeding tendencies, venereal disease etc.<br />
<font color="#FF3300" size="+3">&times;</font> Major surgery in the last 6 months.<br />
<font color="#FF3300" size="+3">&times;</font> Vaccination in the last 24 hours.<br />
<font color="#FF3300" size="+3">&times;</font> Had a miscarriage in the last 6 months or have been pregnant / lactating in the last one year.<br />
<font color="#FF3300" size="+3">&times;</font> Had fainting attacks during last donation.<br />
<font color="#FF3300" size="+3">&times;</font> Have regularly received treatment with blood products.<br />
<font color="#FF3300" size="+3">&times;</font> Shared a needle to inject drugs/ have history of drug addiction.<br />
<font color="#FF3300" size="+3">&times;</font> Had sexual relations with different partners or with a high risk individual.<br />
<font color="#FF3300" size="+3">&times;</font> Been tested positive for antibodies to HIV.<br />

